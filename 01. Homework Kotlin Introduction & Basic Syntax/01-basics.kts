// Declaring variables for the trapezoid
val a: Int = 5
val b: Float = 6.25F
val c: Float = 4.5F
val d: Float = 5.5F
val h: Float = 3.825F

// Perimeter of a trapezoid
val p: Float = a + b + c + d
println(p)

// Surface of a trapezoid
val s: Float = ((a + b) / 2) * h
println(s)

// Declaring variable for the circle
val r: Float = 12.755F

// Area of a circle
val area: Double = r * r * Math.PI
println(area)

// Circumference of a circle
val circumference: Double = r * 2 * Math.PI
println(circumference)